    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String name = txtname.getText();
        String tp = txtnumber.getText();
        String address = txtaddress.getText();
        if (name.isEmpty() || tp.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all the feilds.");
            return;
        }

        String query = "INSERT INTO employee (name, tp_number, address) VALUES (?, ?, ?)";
        try ( Connection conn = DBConnect.getConnection();  PreparedStatement pst = conn.prepareStatement(query)) {
            pst.setString(1, name);
            pst.setString(2, tp);
            pst.setString(3, address);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Employee added successfully!");
            loadEmplyees("");
            txtname.setText("");
            txtnumber.setText("");
            txtaddress.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error adding Employee: " + e.getMessage());
        }

    }//GEN-LAST:event_btnsaveActionPerformed

