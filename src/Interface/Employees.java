    public void loadEmplyees(String keyword) {
        Connection conn = DBConnect.getConnection();
        DefaultTableModel model = (DefaultTableModel) employeetable.getModel();
        model.setRowCount(0); // Clear table

        String query = "SELECT * FROM employee WHERE name LIKE ? OR tp_number LIKE ? OR address LIKE ?";
        try ( PreparedStatement pst = conn.prepareStatement(query)) {
            String searchPattern = "%" + keyword + "%";
            pst.setString(1, searchPattern);
            pst.setString(2, searchPattern);
            pst.setString(3, searchPattern);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("employee_id"),
                    rs.getString("name"),
                    rs.getString("tp_number"),
                    rs.getString("address")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error searching employee: " + e.getMessage());
        }
    }

