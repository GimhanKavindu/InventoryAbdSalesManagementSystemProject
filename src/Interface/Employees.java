    private void btnremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveActionPerformed
        // TODO add your handling code here:
        int sectedrow = employeetable.getSelectedRow();
        if (sectedrow >= 0) {
            int id = (int) employeetable.getValueAt(sectedrow, 0);

            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure?", "Delete", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                String query = "DELETE FROM Employee WHERE employee_id = ?";
                try ( Connection conn = DBConnect.getConnection();  PreparedStatement pst = conn.prepareStatement(query)) {
                    pst.setInt(1, id);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Employee deleted.");
                    loadEmplyees("");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error deleting Employee: " + e.getMessage());
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please select a Employee to delete.");
        }


    }//GEN-LAST:event_btnremoveActionPerformed

