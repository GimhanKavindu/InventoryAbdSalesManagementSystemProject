    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
       String name = txtname.getText();
        String tp = txtnumber.getText();
        String address = txtaddress.getText();
        if (name.isEmpty() || tp.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all the feilds.");
            return;
        }

        String query = "UPDATE supplier SET name = ?, tp_number = ?, address = ? WHERE supplier_id= ? ";
        try ( Connection conn = DBConnect.getConnection();  PreparedStatement pst = conn.prepareStatement(query)) {
            pst.setString(1, name);
            pst.setString(2, tp);
            pst.setString(3, address);
            pst.setInt(4, supplierId);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Supplier updated successfully!");
            loadSuppliers("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error updating supplier: " + e.getMessage());
        }
        
        
    }//GEN-LAST:event_btnupdateActionPerformed

